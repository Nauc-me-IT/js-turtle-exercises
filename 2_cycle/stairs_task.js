/*
Nakresli schody!

Použitím příkazů
 -  forward(steps) - jdi dopředu určitý počet kroků
 -  right(angle) - zatoč doprava pod určitým úhlem
 -  left(angle) - zatoč doleva pod určitým úhlem
 nakresli 5 stupínků schodů. Hrany stupínků mají 50 kroků.

Uděláš to jednoduše. Uprav následují for cyklus.
*/

